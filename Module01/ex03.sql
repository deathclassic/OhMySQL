SELECT author, COUNT(author), COUNT(amount), COUNT(*)
FROM book
GROUP BY author;

SELECT author as Автор, Count(title) as Различных_книг,
	SUM(amount) as Количество_экземпляров
FROM book
GROUP BY author;

/* К групповым функциям SQL относятся: MIN(), MAX() и AVG(), 
которые вычисляют минимальное, максимальное и среднее значение 
элементов столбца, относящихся к группе. */

SELECT author, MIN(price) as Минимальная_цена
FROM book
GROUP BY author;

SELECT author, MIN(price) as Минимальная_цена,
	MAX(price) as Максимальная_цена,
    AVG(price) as Средняя_цена
FROM book
GROUP BY author;

SELECT author, SUM(price * amount) AS Стоимость
FROM book
GROUP BY author;

SELECT author, ROUND(AVG(price), 2) as Средняя_цена
FROM book
GROUP BY author;

SELECT author, SUM(amount)
FROM book
GROUP BY author;

/* Для каждого автора вычислить суммарную стоимость книг S , 
а также вычислить налог на добавленную стоимость  для полученных сумм, который включен 
в стоимость и составляет k = 18%,  а также стоимость книг без него. 
Значения округлить до двух знаков после запятой. 
В запросе для расчета НДС  и Стоимости без НДС */

SELECT author, SUM(price * amount) as Стоимость,
	SUM(ROUND(((price * amount * 0.18) / 1.18), 2)) as НДС,
    SUM(ROUND(price * amount / 1.18, 2)) as Стоимость_без_НДС
FROM book
GROUP BY author;

