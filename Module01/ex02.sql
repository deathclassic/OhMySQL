/* Логическое выражение после ключевого слова WHERE кроме операторов сравнения
и выражений может включать  логические операции (И «and», ИЛИ «or», НЕ «not») 
и круглые скобки, изменяющие приоритеты выполнения операций. 

Пример. Вывести название, автора и цену тех книг, которые написал Булгаков или Есенин, 
ценой больше 600 рублей */

SELECT title, author, price
	FROM book
    WHERE price > 600 and (author = 'Булгаков М.А.' or author = 'Есенин С.А.');

/* Задача. Вывести название, автора,  цену  и количество всех книг,
цена которых меньше 500 или больше 600, а стоимость всех экземпляров
этих книг больше или равна 5000. */

SELECT title, author, price, amount
	FROM book
    WHERE (price < 500 or price > 600) and amount * price > 5000;
    
/*  Логическое выражение после ключевого слова WHERE может включать операторы
BETWEEN и IN. Приоритет  у этих операторов такой же как у операторов сравнения,
то есть они выполняются раньше, чем NOT, AND, OR. */

# Пример. Выбрать названия и цены книг, написанных Булгаковым или Достоевским.

SELECT title, author, price
