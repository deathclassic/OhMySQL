/* Чтобы отобрать уникальные элементы некоторого столбца используется
ключевое слово DISTINCT, которое размещается сразу после SELECT. */

USE stepik;
SELECT DISTINCT author FROM book;

/*С помощью GROUP BY можно выбрать уникальные элементы столбца, по 
которому осуществляется группировка. Результат будет точно такой же 
как при использовании DISTINCT. */

SELECT amount FROM book GROUP BY amount;

/* При группировке над элементами столбца, входящими в группу можно 
выполнить различные действия, например, просуммировать их или найти 
количество элементов в группе c помощью SUM и Count. */

SELECT author, SUM(amount)
FROM book
GROUP BY author;

INSERT INTO book (title, author, price, amount)
