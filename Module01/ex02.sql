/* Задача 1.
Для каждой книги из таблицы book установим скидку следующим образом:
если количество книг меньше 4, то скидка будет составлять 50% от цены,
в противном случае 30%.
tip: IF(логическое_выражение, выражение_если_true, выражение_усли_false) */

SELECT title, author, amount,
	ROUND(IF(amount < 4, price * 0.5, price * 0.7), 2) as new_price
    FROM book;

/* Задача 2. При анализе продаж книг выяснилось, что наибольшей популярностью
пользуются книги Михаила Булгакова, на втором месте книги Сергея Есенина.
Исходя из этого решили поднять цену книг Булгакова на 10%, а цену книг Есенина - на 5%.
Написать запрос, куда включить автора, название книги и новую цену,
последний столбец назвать new_price. Значение округлить до двух знаков после запятой. */

SELECT author, title, ROUND(IF(author = 'Булгаков М.А.', price * 1.1,
	IF(author = 'Есенин С.А.', price * 1.05, price)), 2) as new_price
    FROM book;

/* С помощью запросов можно включать в итоговую выборку не все строки исходной таблицы,
 а только те, которые отвечают некоторому условию, с помощью WHERE */
 
