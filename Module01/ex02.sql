/* Вывести название и авторов тех книг, цены которых принадлежат интервалу 
от 540.50 до 800 (включая границы), а количество или 2, или 3, или 5, или 7 . */

SELECT title, author
	FROM book
    WHERE (price BETWEEN 540.50 and 800)
    and (amount in (2, 3, 5, 7));

/* При выборке можно указывать столбец или несколько столбцов, по которым
необходимо отсортировать отобранные строки. Для этого используются ключевые слова
ORDER BY, после которых задаются имена столбцов. При этом строки сортируются по 
первому столбцу, если указан второй столбец, сортировка осуществляется только 
для тех строк, у которых значения первого столбца одинаковы. По умолчанию ORDER BY 
выполняет сортировку по возрастанию. Чтобы управлять направлением сортировки вручную, 
после имени столбца указывается ключевое слово ASC (по возрастанию) или DESC (по убыванию). */

# Сортировка в алфовитном порядке по названию

SELECT title, author, price
	FROM book
	ORDER BY title;

/* Пример. Вывести автора, название и количество книг, в отсортированном 
в алфавитном порядке по автору и по убыванию количества, для тех книг, 
цены которых меньше 750 рублей */

SELECT author, title, amount
	FROM book
    WHERE price < 750
    ORDER BY author, amount desc;
    
/* Задача. Вывести  автора и название  книг, количество которых принадлежит 
интервалу от 2 до 14 (включая границы). Информацию  отсортировать сначала по 
авторам (в обратном алфавитном порядке), а затем по названиям книг (по алфавиту). */

SELECT author, title, amount
	FROM book
    WHERE amount BETWEEN 2 and 14
    ORDER by author desc, title;
    
